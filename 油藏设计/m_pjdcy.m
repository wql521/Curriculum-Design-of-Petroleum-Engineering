function y = m_pjdcy(x)
    % 函数 m_pjdcy 计算正弦和拟合值
    % 输入:
    %   x - 自变量
    % 输出:
    %   y - 因变量值

    % 拟合参数
    a1 = 1.5664e+03;
    b1 = 0.0868;
    c1 = -0.7621;
    
    a2 = 1.4281e+03;
    b2 = 0.0912;
    c2 = 2.2504;
    
    a3 = 6.7212;
    b3 = 0.4669;
    c3 = -0.1999;

    % 计算函数值
    y = a1 * sin(b1 * x + c1) + ...
        a2 * sin(b2 * x + c2) + ...
        a3 * sin(b3 * x + c3);
end
